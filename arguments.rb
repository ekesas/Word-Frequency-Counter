class Calculator 

	def addition (num1, num2)

		num1 + num2

	end 
	

	def subtraction (num1, num2)	

		num1 - num2

	end 
	
	def division(num1, num2)	

		num1/num2

	end
	
	def multiplication (num1, num2)	

		num1 * num2

	end 
	
	def full_name (name1, name2)	

		puts "Full name is " + name1 + name2+"."

	end 
	
	def str_length(word)	

		puts word.length

	end	

end 



